package agrVai;

public class Calculadora{ 
	
	public float Somar(float a, float b){
		return (a + b);
	}
	
	public float subtrair(float a, float b) {
		return (a-b);
	}
	
	public float multiplicar(float a, float b) {
		return (a * b);
	}
	
	public float dividir(float a, float b) {
		return (a / b);
	}
}
